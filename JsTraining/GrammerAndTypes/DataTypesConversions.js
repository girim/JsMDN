/* JS is dynamically typed language */

var number = 44;
console.log(typeof number);

number = "some text";
console.log(typeof number);

var msg = 44 + " some Text"; //order does not matter
console.log(msg);
console.log(typeof msg);

var diff = '20' - 5;
console.log(diff);
console.log(typeof diff);

var sum = +'9' + +'10';
console.log(sum);